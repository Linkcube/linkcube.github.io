<div class="content">
	<h1 class="scroll-m-20 text-4xl font-extrabold tracking-tight lg:text-5xl">
		<a class="hover:underline" href="https://github.com/Linkcube/Scanlation-Harmonia"
			>Scanlation Harmonia</a
		>
	</h1>
	<p class="leading-7 [&:not(:first-child)]:mt-6">
		A scanlation management program built for the very defunct Chi-Fi Scans.
	</p>

	<h4 class="mt-6 scroll-m-20 border-b text-xl font-semibold tracking-tight">Why Build It?</h4>
	<p class="leading-7 [&:not(:first-child)]:mt-3">
		After completing the svelte radio interface, and really enjoying svelte, I thought to extend
		this suite of tools to another hobbey of mine. At the time, I was actively scanlating a
		relatively osbcure series and thought that having an app that could keep track of various things
		in a graphic would be better than the various text files I was using at the time.
	</p>

	<h4 class="mt-6 scroll-m-20 border-b text-xl font-semibold tracking-tight">Features</h4>
	<p class="leading-7 [&:not(:first-child)]:mt-3">
		While this does have the basic features you would imagine needing to manage translations for an
		image, it also had:
	</p>
	<ul class="my-6 ml-6 list-disc [&>li]:mt-2">
		<li>Support for translating to multiple languages</li>
		<li>Quick view swapping between raw/cleaned/redraw/translated images</li>
		<li>Graphical overlay to track text boxes</li>
		<li>Typesetting tracking</li>
		<li>Bundling export system</li>
	</ul>
	<p class="leading-7">
		For more details, there is a static site for the program
		<a class="underline" href="https://scanlation-harmonia.netlify.app/">here</a>.
	</p>
